    public void verRegistro(){
        try{
            Date i = (Date)this.fInicio.getValue();
            Date f = (Date)this.fFin.getValue();
            double tmp=0;
            String tt=this.tTmp.getText();
            if(!tt.trim().isEmpty())
                tmp = Double.parseDouble(this.tTmp.getValue().toString());
            GListRegister gr = new GListRegister();
            List<GRegister> lista = gr.getReportDayTmp(i, f, tmp);
            this.model.setNumRows(0);
            for(GRegister l: lista){
                String fecha = DateTime.getDate(l.getDate());
                Object datos[] = {l.getPerson().getCc(), l.getPerson().getName(), l.getPerson().getLastName(),
                                  fecha,l.getTemperature(),l.getSymptoms(),l.getOrigin(),
                                  l.getDestination(), l.getObrsevation()};
                this.model.addRow(datos);
            }
            this.lTotal.setVisible(true);
            this.lValorTotal.setVisible(true);
            this.lValorTotal.setText(""+lista.size());
            
        }catch(Exception ex){
            JOptionPane.showMessageDialog(this, ex.getMessage(), "ERROR", JOptionPane.ERROR_MESSAGE);
        }    
    }
    
    
